import random
import datetime


def get_time() -> str:
    """Вернуть текущее время красивой строкой."""
    now = datetime.datetime.now()
    return f"Сейчас {now.hour:02d}:{now.minute:02d}"


def process_command(command: str) -> str:
    """
    Базовая логика ответов MoriX.
    Принимает сырую строку команды и возвращает текст-ответ.
    """
    if command is None:
        command = ""
    command = command.strip().lower()

    # Приветствия
    greetings = [
        "Дарова!",
        "Салам алейкум, брат!",
        "Приветствую, смертный!",
        "Йо, дружище!",
        "О, а вот и ты!",
        "Здарова, работяга!",
    ]

    if command in ["привет", "здравствуй", "салам", "добрый день", "hi", "hello"]:
        return random.choice(greetings)

    # О себе
    if command in ["что ты умеешь", "что ты можешь", "расскажи о себе", "кто ты"]:
        return (
            "Я MoriX – твой личный ассистент. Пока что отвечаю на простые команды "
            "и помогаю с базовыми задачами, но ты меня обязательно прокачаешь."
        )

    # Анекдоты / шутки
    if command in ["анекдот", "расскажи шутку", "шутка", "пошути"]:
        jokes = [
            "Программист пришёл в бар, заказал 1 пиво. Бармен спросил: «Вам налить 1 пиво или 0 пива?»",
            "Почему программисты путают Хэллоуин и Рождество? Потому что 31 октября — это 25 декабря в шестнадцатеричной системе.",
            "Если в коде работает – не трогай. Если не работает – открой StackOverflow!",
        ]
        return random.choice(jokes)

    # Время
    if command in ["время", "сколько времени", "который час"]:
        return get_time()

    # Заглушка по умолчанию
    if not command:
        return "Скажи что-нибудь, а я попробую ответить."

    return f"Ты сказал: «{command}». Пока я отвечаю просто, но со временем стану умнее."
